@pytest.mark.django_db(transaction=True)
async def test_canvas_write_tools_forbidden_for_read_role(workspace, read_user, semantic_model):
    from apps.agents.tools.canvas_tool import create_canvas_tools

    thread = await Thread.objects.acreate(workspace=workspace, user=read_user)
    tools = {t.name: t for t in create_canvas_tools(workspace, read_user, str(thread.id))}

    apply_result = await tools["canvas_apply"].ainvoke(
        {"operations": [{"op": "add_existing", "object_type": "dataset", "ref": "raw_visits"}]}
    )
    assert apply_result["errors"][0]["code"] == "FORBIDDEN"

    commit_result = await tools["canvas_commit"].ainvoke({})
    assert commit_result["errors"][0]["code"] == "FORBIDDEN"

    assert await SemanticCanvasChange.objects.acount() == 0
    # Reads stay available to read-only members, matching the REST policy.
    read_result = await tools["canvas_read"].ainvoke({"selector": "graph"})
    assert "Canvas is empty" in read_result


@pytest.mark.django_db
def test_build_tools_gates_canvas_manager_on_write_role(workspace, user):
    from apps.agents.graph.base import _build_tools

    writable = {
        t.name
        for t in _build_tools(
            workspace, user, [], conversation_id="t1", interactive=True, canvas_write=True
        )
    }
    assert {"canvas_read", "canvas_manager"} <= writable

    readonly = {
        t.name
        for t in _build_tools(
            workspace, user, [], conversation_id="t1", interactive=True, canvas_write=False
        )
    }
    assert "canvas_read" in readonly
    assert "canvas_manager" not in readonly


@pytest.mark.django_db
def test_thread_canvas_api_flow(client, workspace, user, semantic_model):
    client.force_login(user)
    thread = Thread.objects.create(workspace=workspace, user=user)
    base = f"/api/workspaces/{workspace.id}/threads/{thread.id}/canvas/"

    projection = client.get(base).json()
    assert projection["objects"] == []
    assert projection["can_commit"] is False

    response = client.post(
        f"{base}apply/",
        {
            "operations": [
                {"op": "set", "target": "dataset/raw_visits/description", "value": "All visits"}
            ]
        },
        content_type="application/json",
    )
    assert response.status_code == 200
    assert response.json()["objects"][0]["state"] == "edited"

    bad = client.post(
        f"{base}apply/",
        {"operations": [{"op": "set", "target": "dataset/raw_visits/nope", "value": "x"}]},
        content_type="application/json",
    )
    assert bad.status_code == 400
    assert bad.json()["errors"][0]["code"] == "INVALID_TARGET"


@pytest.mark.django_db
def test_thread_canvas_api_requires_write_role(client, workspace, read_user, semantic_model):
    client.force_login(read_user)
    thread = Thread.objects.create(workspace=workspace, user=read_user)
    base = f"/api/workspaces/{workspace.id}/threads/{thread.id}/canvas/"

    assert client.get(base).status_code == 200
    response = client.post(f"{base}apply/", {"operations": []}, content_type="application/json")
    assert response.status_code == 403



def _visits(semantic_model):
    return semantic_model.datasets.get(name="raw_visits")


def test_apply_stages_dataset_edit_with_minimal_delta(canvas, semantic_model):
    result = apply_operations(
        canvas,
        [
            {"op": "add_existing", "object_type": "dataset", "ref": "raw_visits"},
            {"op": "set", "target": "dataset/raw_visits/description", "value": "All visits"},
        ],
    )

    assert "errors" not in result
    change = canvas.changes.get()
    assert change.change_type == SemanticCanvasChange.ChangeType.UPDATE
    assert change.fields == {"description": "All visits"}
    projection = canvas_projection(canvas)
    entry = projection["objects"][0]
    assert entry["state"] == "edited"
    assert entry["diff"]["description"]["to"] == "All visits"
    assert projection["can_commit"] is True

    # Setting the value back to base removes the delta -> membership row.
    apply_operations(
        canvas, [{"op": "set", "target": "dataset/raw_visits/description", "value": ""}]
    )
    change.refresh_from_db()
    assert change.fields == {}
    assert canvas_projection(canvas)["objects"][0]["state"] == "unchanged"



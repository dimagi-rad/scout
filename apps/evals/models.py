"""
Eval models for the Scout free-SQL-vs-Cube adoption eval loop.

Provides:
- GoldenQuery: A curated natural-language question with optional reference SQL
  and an expected-answer description. Used as the input set for eval runs.
- EvalRun: A single comparison run of one golden query, recording outputs and
  timing from both the free-SQL path and the Cube semantic-layer path, plus
  structured scoring fields.
"""

import uuid

from django.core.validators import MaxValueValidator, MinValueValidator
from django.db import models


class GoldenQuery(models.Model):
    """
    A curated question used as the input for eval runs.

    Stores the natural-language question, an optional reference SQL query,
    and a plain-language description of the expected answer shape so that
    automated judges and human reviewers have a ground-truth anchor.
    """

    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    workspace = models.ForeignKey(
        "workspaces.Workspace",
        on_delete=models.CASCADE,
        related_name="golden_queries",
        null=True,
        blank=True,
    )

    title = models.CharField(max_length=255)
    question = models.TextField(help_text="The natural-language question posed to the agent.")
    reference_sql = models.TextField(
        blank=True,
        help_text="Optional baseline SQL that correctly answers the question.",
    )
    expected_summary = models.TextField(
        blank=True,
        help_text="Plain-language description of the expected answer (shape, values, caveats).",
    )
    source = models.CharField(
        max_length=50,
        blank=True,
        help_text='Origin of this query. E.g. "user_provided", "extracted_from_chat".',
    )

    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        ordering = ["title"]

    def __str__(self) -> str:
        return self.title


class EvalRun(models.Model):
    """
    A single comparison run of a GoldenQuery against both execution paths.

    Records raw outputs, row counts, and timing from the free-SQL path and the
    Cube semantic-layer path, then captures structured scoring: boolean match,
    float confidence, and a semantic-equivalence label.
    """

    SEMANTIC_EQUIVALENCE_CHOICES = [
        ("exact", "Exact match"),
        ("approximate", "Approximate match"),
        ("failed", "Failed / no match"),
    ]

    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    workspace = models.ForeignKey(
        "workspaces.Workspace",
        on_delete=models.CASCADE,
        related_name="eval_runs",
        null=True,
        blank=True,
    )
    golden_query = models.ForeignKey(
        GoldenQuery,
        on_delete=models.CASCADE,
        related_name="runs",
    )

    # Free-SQL path outputs
    free_sql = models.TextField(blank=True, help_text="SQL generated by the free-SQL path.")
    free_sql_result = models.JSONField(
        null=True,
        blank=True,
        help_text="Result set returned by the free-SQL path (serialised rows).",
    )
    free_sql_ms = models.IntegerField(
        null=True,
        blank=True,
        help_text="Execution time of the free-SQL path in milliseconds.",
    )

    # Cube semantic-layer path outputs
    cube_query = models.TextField(
        blank=True,
        help_text="Cube REST query JSON or member expression sent to the Cube API.",
    )
    cube_result = models.JSONField(
        null=True,
        blank=True,
        help_text="Result set returned by the Cube path (serialised rows).",
    )
    cube_ms = models.IntegerField(
        null=True,
        blank=True,
        help_text="Execution time of the Cube path in milliseconds.",
    )

    # Scoring
    result_match = models.BooleanField(
        null=True,
        help_text="True when both paths produced equivalent results.",
    )
    match_confidence = models.FloatField(
        null=True,
        validators=[MinValueValidator(0.0), MaxValueValidator(1.0)],
        help_text="Judge confidence in the result_match assessment (0.0-1.0).",
    )
    semantic_equivalence = models.CharField(
        max_length=20,
        choices=SEMANTIC_EQUIVALENCE_CHOICES,
        blank=True,
        help_text="Categorical equivalence label assigned by the LLM judge.",
    )
    used_preaggregation = models.BooleanField(
        default=False,
        help_text="True when the Cube path was served from a pre-aggregation (rollup).",
    )

    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ["-created_at"]
        indexes = [
            models.Index(fields=["golden_query", "created_at"]),
        ]

    def __str__(self) -> str:
        return f"EvalRun({self.golden_query_id}, {self.created_at})"
